`timescale 1ns / 1ps
//////////////////////////////////////////////////////////////////////////////////
// Module Name: Mul_33
// Description: 组合逻辑有符号 33x33 乘法器，输出 66 位有符号积。
//   - 不使用 IP 核，不使用 * / % 运算符（只用 +/-、移位、与）。
//   - 单周期组合电路，满足"至多 5 个周期给出正确结果"的要求。
//   - 输入约定：调用方已把 32 位操作数符号扩展(mul/mulh)或零扩展(mulhu)到 33 位，
//     因此这里统一按 33 位有符号数相乘即可覆盖 mul / mulh / mulhu。
//   - 原理：b 为 33 位有符号数，b = sum(b[i]*2^i, i=0..31) - b[32]*2^32，
//     故 a*b = sum(b[i]*(a<<i)) - b[32]*(a<<32)，最高位取负权重实现有符号乘。
//////////////////////////////////////////////////////////////////////////////////

module Mul_33(
    input       [32: 0]     a,
    input       [32: 0]     b,
    output      [65: 0]     res
);
    // 把 33 位有符号被乘数符号扩展到 66 位
    wire [65:0] a_ext = {{33{a[32]}}, a};

    integer k;
    reg [65:0] acc;
    always @(*) begin
        acc = 66'd0;
        // 低 32 位为正权重
        for (k = 0; k < 32; k = k + 1) begin
            if (b[k]) acc = acc + (a_ext << k);
        end
        // 第 32 位（符号位）为负权重
        if (b[32]) acc = acc - (a_ext << 32);
    end

    assign res = acc;
endmodule
